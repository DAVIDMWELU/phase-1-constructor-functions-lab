// Scooter Constructor
function Scooter(year, color, model) {
    this.year = year;
    this.color = color;
    this.model = model;
  }
  
  // Driver Constructor
  function Driver(name, age, experience) {
    this.name = name;
    this.age = age;
    this.experience = experience;
  }
  
  // PickupLocation Constructor
  function PickupLocation(address, city) {
    this.address = address;
    this.city = city;
  }
  